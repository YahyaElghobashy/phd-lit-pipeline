#!/usr/bin/env python3
"""
PhD Literature Extraction Pipeline — Gap Taxonomy
===================================================
Classifies research gaps into tiers: Core, Supporting, Niche.

Core gaps drive the dissertation and get full Opus deep analysis.
Supporting gaps provide context and get lighter Sonnet analysis.
Niche gaps are logged in GAP_TRACKER but excluded from the elimination matrix.

Usage:
    python3 gap_taxonomy.py --classify          # Classify all gaps (writes to sheet)
    python3 gap_taxonomy.py --classify-dry-run  # Preview classifications
    python3 gap_taxonomy.py --stats             # Show tier distribution
    python3 gap_taxonomy.py --override GAP_001 Core "Central to Paper 2"
"""
from __future__ import annotations

import argparse
import json
import os
import subprocess
import sys
import threading
import time
from datetime import datetime
from pathlib import Path

from rich.console import Console
from rich.panel import Panel
